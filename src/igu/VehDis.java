    private void ConBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConBtnActionPerformed
        String nombreArchivo = "Vehiculos.xlsx";
        File archivo = new File(nombreArchivo);
        modeloE.Importar(archivo, jtDatos);
        String []info=new String[6];
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        String FecIniCon = FecIniTf.getText();
        String FecFinCon = FecFinTf.getText();
        for (int i=0; i<jtDatos.getRowCount(); i++){
            String Pla = String.valueOf(jtDatos.getValueAt(i, 0));
            String Mar = String.valueOf(jtDatos.getValueAt(i, 1));
            String Col = String.valueOf(jtDatos.getValueAt(i, 2));
            String Tra = String.valueOf(jtDatos.getValueAt(i, 3));
            String Cat = String.valueOf(jtDatos.getValueAt(i, 4));
            String NumSed = String.valueOf(jtDatos.getValueAt(i, 5));
            String FecIni = String.valueOf(jtDatos.getValueAt(i, 7));
            String FecFin = String.valueOf(jtDatos.getValueAt(i, 8));
            if (NumSed.equals(NumSedTf.getText())){
                try {
                    Date inicio1 = formato.parse(FecIniCon);
                    Date fin1 = formato.parse(FecFinCon);
                    Date inicio2 = formato.parse(FecIni);
                    Date fin2 = formato.parse(FecFin);
                    if ((fin1.before(inicio2)&&inicio1.before(inicio2))||(fin2.before(inicio1)&&inicio2.before(inicio1))) {
                        info[0] = Pla;
                        info[1] = Mar;
                        info[2] = Col;
                        info[3] = Tra;
                        info[4] = Cat;
                        info[5] = NumSed;
                        nuevTabl.addRow(info);
                    }
                } catch(ParseException e){
                    e.printStackTrace();
                }
            }
        }
        TabVehDis ventaTabVehDis = new TabVehDis(nuevTabl);
        ventaTabVehDis.setVisible(true);
        ventaTabVehDis.setLocationRelativeTo(null);
        this.setVisible(false);
    }//GEN-LAST:event_ConBtnActionPerformed

